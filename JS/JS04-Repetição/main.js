// While

let contador = 0 // Inicialização
// Enquanto a variável contador for menor que 10 o "while" apresentará o valor da variável no console
while(contador <= 10){
    console.log(contador)
    // contador = contador + 1
    // contador +=1
    contador++ 

}
console.log("Chegou ao fim da contagem")

//Laço For
// variável inicial; condição; incremento
contador = 0
for(contador = 0; contador <= 10; contador++){
    console.log(contador)

}
console.log("Acaboouu")
